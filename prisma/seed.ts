async function main() {
  console.log("🌱 Seeding database...");

  // Upsert system user
  const systemUser = await prisma.user.upsert({
    where: { email: SYSTEM_EMAIL },
    update: {},
    create: {
      email: SYSTEM_EMAIL,
      name: "BittsQuiz Team",
      image: null,
    },
  });
  console.log(`✅ System user: ${systemUser.id}`);

  // Seed packs
  for (const pack of PACKS_DATA) {
    await prisma.pack.upsert({
      where: { slug: pack.slug },
      update: pack,
      create: pack,
    });
  }
  console.log(`✅ ${PACKS_DATA.length} packs seeded`);

  // Seed quizlets
  for (const q of QUIZLETS_DATA) {
    await prisma.quizlet.upsert({
      where: { name: q.name },
      update: {
        ...q,
        sellValue: SELL_VALUES[q.rarity] ?? 10,
      },
      create: {
        ...q,
        sellValue: SELL_VALUES[q.rarity] ?? 10,
      },
    });
  }
  console.log(`✅ ${QUIZLETS_DATA.length} quizlets seeded`);

  // Seed quizzes
  let quizCount = 0;
  let questionCount = 0;
  for (const quiz of QUIZZES) {
    const existing = await prisma.quiz.findFirst({
      where: { title: quiz.title, isOfficial: true },
    });
    if (!existing) {
      const created = await prisma.quiz.create({
        data: {
          title: quiz.title,
          description: quiz.description,
          category: quiz.category,
          difficulty: quiz.difficulty,
          isOfficial: true,
          isNew: ("isNew" in quiz && quiz.isNew === true) ? true : false,
          authorId: systemUser.id,
          questions: {
            create: quiz.questions.map((q, i) => ({
              text: q.text,
              options: q.options,
              correctIndex: q.correctIndex,
              points: 1,
              order: i,
              ...("imageUrl" in q && q.imageUrl ? { imageUrl: q.imageUrl } : {}),
              ...("explanation" in q && typeof q.explanation === "string" ? { explanation: q.explanation } : {}),
            })),
          },
        },
      });
      quizCount++;
      questionCount += quiz.questions.length;
      void created;
    }
  }
  console.log(`✅ ${quizCount} quizzes + ${questionCount} questions seeded`);
  console.log("🎉 Seeding complete!");
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
